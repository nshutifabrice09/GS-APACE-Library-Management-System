    public void clearTable(){
        DefaultTableModel model = (DefaultTableModel) bookTable.getModel();
        model.setRowCount(0);
        
    }
    
    public boolean updateBook(){
        boolean isUpdated = false;
       bookId = Integer.parseInt(bookIdTxt.getText());
       bookName = bookNameTxt.getText();
       quantity = Integer.parseInt(quantityTxt.getText());
       classes = yearTxt.getText();
       try{
           Connection con = DriverManager.getConnection(db_url, db_username, db_passwd);
           String sql = "update books set book_name = ?, quantity = ?, classes = ? where book_id = ?";
           PreparedStatement pst = con.prepareStatement(sql);
           pst.setString(1, bookName);
           pst.setInt(2, quantity);
           pst.setString(3, classes);
           pst.setInt(4, bookId);
           
           int rowCount = pst.executeUpdate();
           if(rowCount > 0){
               isUpdated = true;
           }else{
               isUpdated = false;
           }
       }catch(Exception ex){
           ex.printStackTrace();
       }
       
    return isUpdated;
    }
   
    public boolean deleteBook(){
        boolean isDeleted = false;
        bookId = Integer.parseInt(bookIdTxt.getText());
       bookName = bookNameTxt.getText();
       quantity = Integer.parseInt(quantityTxt.getText());
       classes = yearTxt.getText();
        
        try{
           Connection con = DriverManager.getConnection(db_url, db_username, db_passwd);
           String sql = "delete from books where book_id = ?";
           PreparedStatement pst = con.prepareStatement(sql);
           pst.setInt(1, bookId);
           pst.executeUpdate();
           
           int rowCount = pst.executeUpdate();
           if(rowCount> 0){
               isDeleted = true;
           }else{
               isDeleted = false;
           }
        }catch(Exception ex){
            ex.printStackTrace();
        }
        
    return isDeleted;
        
    }
    
    
