   public void SetBookToTable(){
       try{
           Connection con = DriverManager.getConnection(db_url, db_username, db_passwd);
           PreparedStatement pst = con.prepareCall("select * from books ");
           ResultSet result = pst.executeQuery();
           while(result.next()){
               int bookId = result.getInt("book_id");
               String bookName = result.getString("book_name");
               int quantity = result.getInt("quantity");
               String classes = result.getString("classes");
               
               Object[] obj = {bookId, bookName, quantity, classes};
               model =(DefaultTableModel ) bookTable.getModel();
               model.addRow(obj);
               
           }
       }catch(Exception ex){
           ex.printStackTrace();
       }
       
       
   } 
   
   public boolean addBook(){
       
       boolean isAdded = false;
       bookId = Integer.parseInt(bookIdTxt.getText());
       bookName = bookNameTxt.getText();
       quantity = Integer.parseInt(quantityTxt.getText());
       classes = yearTxt.getText();
       try{
           Connection con = DriverManager.getConnection(db_url, db_username, db_passwd);
           String sql = "insert into books values(?,?,?,?)";
           PreparedStatement pst = con.prepareStatement(sql);
           pst.setInt(1, bookId);
           pst.setString(2, bookName);
           pst.setInt(3, quantity);
           pst.setString(4, classes);
           
           int rowCount = pst.executeUpdate();
           if(rowCount >0){
               isAdded =true;
               
           }else{
               isAdded =false;
           }
           
       }catch(Exception ex){
           ex.printStackTrace();
       }
    return isAdded;
       
   }
   
